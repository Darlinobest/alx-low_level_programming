#include <stdio.h>

/**
 * add - adds two numbers
 * @a: first input
 * @b: second input
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substracts two numbers
 * @a: first input
 * @b: second input
 * Return: different
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: first input
 * @b: second input
 * Return: product of two numbers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: first input
 * @b: second input
 * Return: different
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - remainder of division
 * @a: first input
 * @b: second input
 * Return: mod
 */

int mod(int a, int b)
{
	return (a % b);
}
